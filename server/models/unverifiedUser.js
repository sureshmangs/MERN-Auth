const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const userSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    token: {
        type: String,
        required: true
    }
}, { timestamps: true });

const UnverifiedUser = mongoose.model('unverified_user', userSchema);

module.exports = UnverifiedUser;